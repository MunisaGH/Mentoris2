def translate_processor(request):
    lang = getattr(request, "LANGUAGE_CODE", "uz")
    translations = TRANSLATIONS.get(lang, TRANSLATIONS["uz"])
    context = {"T": translations}
    context["google_auth_enabled"] = settings.GOOGLE_AUTH_ENABLED
    if request.user.is_authenticated:
        context["unread_notifications_count"] = Notification.objects.filter(
            user=request.user,
            is_read=False,
        ).count()
    return context
